CREATE TABLE Actor {
	ID_Actor INT NOT NULL PRIMARY KEY IDENTITY(1,1),
    Nama_Actor VARCHAR(50) NOT NULL,
    ID_Film INT NOT NULL FOREIGN KEY (ID_Film) REFERENCES Film(ID_Film)
}

CREATE TABLE Film {
	ID_Film INT NOT NULL PRIMARY KEY IDENTITY(1,1),
	Nama_Film VARCHAR(50) NOT NULL,
	ID_Genre INT NOT NULL FOREIGN KEY (ID_Genre) REFERENCES Genre(ID_Genre)
}

CREATE TABLE Genre {
	ID_Genre INT NOT NULL PRIMARY KEY IDENTITY(1,1),
	Nama_Genre VARCHAR(50) NOT NULL,
}

CREATE TABLE Admin {
	ID_Admin INT NOT NULL PRIMARY KEY IDENTITY(1,1),
	Username_Admin VARCHAR(50) NOT NULL,
	Password_Admin VARCHAR (50) NOT NULL
}

CREATE TABLE Pelanggan {
	ID_Pelanggan INT NOT NULL PRIMARY KEY IDENTITY(1,1),
	Username_Pelanggan VARCHAR(50) NOT NULL,
	Password_Pelanggan VARCHAR(50) NOT NULL
}

CREATE TABLE Penyewaan {
	ID_Penyewaan INT NOT NULL PRIMARY KEY IDENTITY(1,1),
	Tanggal DATE NOT NULL,
	ID_Film INT NOT NULL FOREIGN KEY (ID_Film) REFERENCES Film(ID_Film),
	ID_Pelanggan INT NOT NULL FOREIGN KEY (ID_Pelanggan) REFERENCES Pelanggan(ID_Pelanggan)
}